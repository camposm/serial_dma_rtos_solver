/**
* @brief Function implementing the motorSimulation thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_motorSimulationTask */
void motorSimulationTask(void *argument)
{
  /* USER CODE BEGIN motorSimulationTask */
    uint16_t written = 0;
    uint32_t sim_time = 0;

    const uint8_t*  left_motor_ptr = get_left_motor_output();
    const uint8_t*  right_motor_ptr = get_right_motor_output();
    const uint32_t  left_motor_output_size = get_left_motor_output_size();
    const uint32_t  right_motor_output_size = get_right_motor_output_size();

    TickType_t last_execution_time = xTaskGetTickCount();

    volatile uint32_t start, delta;
    volatile uint32_t start0, delta0;

    for(;;)
    {
        start = TIM2->CNT;
        start0 = HAL_GetTick();

        solve_10ms(sim_time);

        delta = TIM2->CNT - start;
        delta0 = HAL_GetTick() - start0;

        written = write_to_tx_buffer(left_motor_ptr, left_motor_output_size);
        if (written != left_motor_output_size)
        {
            while (1);
        }

        written = write_to_tx_buffer(right_motor_ptr, right_motor_output_size);
        if (written != right_motor_output_size)
        {
            while (1);
        }

        sim_time += 1;
        vTaskDelayUntil(&last_execution_time, 10);
    }
  /* USER CODE END motorSimulationTask */
}

